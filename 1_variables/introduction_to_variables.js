//there are several ways of defining a variable in JavaScript
let age = 10;
//Variables defined with 'const' cannot be re-assigned. This means you can use the = sign only once when defining the variable.
const salary = 50000;
var name = "Paul";

console.log(age);
console.log(salary);
console.log(name);

//MY POINT: Avoid using 'var' when defining 1_variables. Use let or const instead.